    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //For deletion
        int rows = jTable1.getSelectedRowCount();
        if (rows > 0) {//Checks if rows are selected
            DefaultTableModel tblmodel = (DefaultTableModel) jTable1.getModel();//Gets table object
            
            for (int i = 0; i < rows; i++) {//Iterates for each selected row
                int IDNo = Integer.valueOf(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0)));//Sets IDNo to ID of current row
                
                tblmodel.removeRow(jTable1.getSelectedRow());//Removes the row
                
                deletedIDs.add(IDNo);//Adds deleted ID to deletedIDs

                //Sets all values to null for the selected customer in cust array
                for (int y = 0; y < 7; y++) {
                    cust[IDNo - 1][y] = null;
                }
            }
            Collections.sort(deletedIDs);
            cId.setText(String.valueOf(deletedIDs.get(0))); //Resets ID field to next free ID
            
            //Prints deletedIDs
            System.out.println("deletedIDs : " + deletedIDs);
            
            if (rows == 1) {
                messagebox("Customer has been deleted.", "Deleted");
            } else {
                messagebox(rows + " Customers have been deleted.", "Deleted");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void cBirthdateKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cBirthdateKeyReleased
        Boolean month = false;
        Boolean day = false;
        Boolean year = false;
        Boolean firstSlash = false;
        Boolean secondSlash = false;
        String birthdate = cBirthdate.getText();
        for (int x = 1; x <= cBirthdate.getText().length(); x++) {
            if (cBirthdate.getText().substring(x-1, x).equals("/") && x > 1) { //month
                firstSlash = true;
                if (Integer.parseInt(birthdate.substring(0, 2)) > 0 && Integer.parseInt(birthdate.substring(0, 2)) < 13) {
                    month = true;
                }
                else {
                        bLabel.setForeground(Color.red);
                        bLabel.setText("Invalid Birthdate");
                }
            }
            if (cBirthdate.getText().substring(x-1, x).equals("/") && firstSlash == true && x > 3) { //day
                secondSlash = true;
                if (Integer.parseInt(birthdate.substring(3, 5)) > 0 && Integer.parseInt(birthdate.substring(3, 5)) < 32) {
                    day = true;
                }
                else {
                        bLabel.setForeground(Color.red);
                        bLabel.setText("Invalid Birthdate");
                }
            }
            if (x > 7) { //year
                if (Integer.parseInt(birthdate.substring(6, 8)) > 3 && Integer.parseInt(birthdate.substring(6, 8)) < 21) {
                    bLabel.setForeground(Color.red);
                    bLabel.setText("Minor");
                    minorChecker = true; //user is underage
                }
                else {
                    minorChecker = false;
                    year = true;
                    if (month == true && day == true && year == true) {
                    bLabel.setForeground(Color.green);
                    bLabel.setText("Valid Birthdate");
                    }
                    else {
                        bLabel.setForeground(Color.red);
                        bLabel.setText("Invalid Birthdate");
                    }
                }
            }
            else {
                bLabel.setForeground(Color.red);
                bLabel.setText("Invalid Birthdate");
            }
        }   
    }//GEN-LAST:event_cBirthdateKeyReleased

